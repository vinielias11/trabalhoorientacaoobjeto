package pacote1;

public abstract class Carro {
	private String possuiOpcaoEletrica;
	private String possuiOpcaoAutomatica;
	
	public Carro(String possuiOpcaoEletrica, String possuiOpcaoAutomatica) {
		this.possuiOpcaoEletrica = possuiOpcaoEletrica;
		this.possuiOpcaoAutomatica = possuiOpcaoAutomatica;
	}
	
}
